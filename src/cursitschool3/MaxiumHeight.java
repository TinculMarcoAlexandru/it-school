package cursitschool3;

public class MaxiumHeight {

        public static void main(String[] args) {
            int heightOne = 165;
            int heightTwo = 172;

            // Using ternary operator to find maximum height
            int maximumHeight = (heightOne > heightTwo) ? heightOne : heightTwo;

            System.out.println("The maximum height is: " + maximumHeight);
        }
    }





